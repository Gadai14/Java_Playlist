public class Type_Conversion {
    public static void main(String[] args) {
        int a=200;
         int d=100000;
         float e=4.59f;
        
        float b=(float)a;
        System.out.println(b);
        double c=(double)d;
        System.out.println(c);
        int f=(int)e;
        System.out.println(f);

    }
}
